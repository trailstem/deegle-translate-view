import type { NextPage } from 'next'
import Head from 'next/head'
import { FontAwesomeIcon } from "@fortawesome/react-fontawesome";
import {faThumbsUp} from "@fortawesome/free-regular-svg-icons";
import {useState} from 'react';
import UserTranslationText from './components/UserTranslationText';
import Header from './components/Header';
import LanguageSwichingTab from './components/LanguageSwichingTab';
import Footer from './components/Footer';



const Home: NextPage = () => {

  // state初期値(入力用)
  const initialValue : any = "";
  // 利用者がしたいテキストの値
  const [inputText, userText] = useState<any>(initialValue);
  // 利用者がGoogle翻訳を使用して変換した後の値
  const [aftTranGoogle, setAftTranGoogle] = useState<any>(initialValue);
  // 利用者がDeepl翻訳を使用して変換した後の値
  const [aftTranDeepl, setAftTranDeepl] = useState<any>(initialValue);

  // 入力値の状態に合わせて、翻訳後のtextareに反映する
  // e: { target: { value: string; }; }
  const translate = (e : any) : void => {
    // e.target.valueで値取得
    userText(e.target.value);
    setAftTranGoogle(e.target.value);
    setAftTranDeepl(e.target.value);
  };

  // Googleのいいね機能
  const goodGoogle = () => {
    console.log('Google翻訳を高く評価しました。');
  };

  // deeplのいいね機能
  const goodDeepl = () => {
    console.log('DeepLを高く評価しました。');
  };

  return (
    <div className="flex-container justify-center">
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <Header/>
      <main>
        <div className="justify-center py-20"> 
          <LanguageSwichingTab/>
            <div className='flex-center px-10 py-6'>
              <UserTranslationText googleText = {inputText} translate = {translate}/>
            </div>

            <div>
              <div className='mt-12'>
                <div className='flex justify-center'>
                  <textarea className='border-2 mx-8 md:h-40 
                  border-rose-500 rounded-lg md:w-1/3 ' value={aftTranGoogle}>
                  </textarea>
                  <textarea  className='border-2 mx-8 md:h-40
                  border-violet-700 rounded-lg md:w-1/3' value={aftTranDeepl}>
                  </textarea>
                </div>
              </div>
          
              <div className='flex justify-center items-center'>
                <button className='w-1/3 mx-8' disabled>
                  <FontAwesomeIcon icon={faThumbsUp} className="fa-2x hover:text-blue-600 transision-all duration-300 cursor-pointer" color='' onClick={goodGoogle}/>
                </button>
                <button className='w-1/3 mx-8' disabled>
                  <FontAwesomeIcon icon={faThumbsUp} className="fa-2x  hover:text-blue-600 transision-all duration-300 cursor-pointer" color='' onClick={goodDeepl}/>
                </button>
              </div>
            </div>
        </div>
      </main>      
      <Footer/>
    </div>
  );
}
export default Home
